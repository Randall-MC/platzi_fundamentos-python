# String
name = "Nicolas"
lastName = 'Mendoza'
print(type(name))
print(type(lastName))

# Numbers
integers = 123
floats = 3.14
print(type(integers))
print(type(floats))

# Boolean
true = True
false = False
print(type(true))
print(type(false))

# List
list = [1, 2, 3, 4, 5]
print(type(list))

# Tuple
tuple = (1, 2, 3, 4, 5)
print(type(tuple))

# Dictionary
dictionary = {"name": "Nicolas", "lastName": "Mendoza", "age": 35}
print(type(dictionary))

# Set
set = {1, 2, 3, 4, 5}
print(type(set))

# Imput
color = input('¿Cuál es tu color favorito? ')
print(type(color))
favorite_number = input('¿Cuál es tu número favorito? ')
print(type(favorite_number))
